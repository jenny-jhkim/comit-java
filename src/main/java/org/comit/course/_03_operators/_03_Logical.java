package org.comit.course._03_operators;

public class _03_Logical {

	public static void main(String[] args) {
		boolean a = true;
		boolean b = false;
		
		boolean and = a && b;
		boolean or = a || b;
		boolean not = !a;
		
		System.out.println("and: " + and);
		System.out.println("or: " + or);
		System.out.println("not: " + not);
		

	}

}

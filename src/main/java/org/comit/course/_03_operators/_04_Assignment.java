package org.comit.course._03_operators;

public class _04_Assignment {
	public static void main(String[] args) {
		int a = 8;
		
		//Addition Assignment
		a +=3; //a = a+3
		System.out.println("+=: " + a);
		
		//Subtraction Assignment
		a -= 3; //a = a - 3;
		System.out.println("-=: " + a);
		
		//Multiplication Assignment
		a *= 2; //a = a * 2;
		System.out.println("*=: " + a);
			
		//Division Assignment
		a /= 4; //a = a / 4;
		System.out.println("/=: " + a);
		
		//Modulus Assignment
		a %= 3; //a = a % 3;
		System.out.println("%=: " + a);
		
		
	}

}

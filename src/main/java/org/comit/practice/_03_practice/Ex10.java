package org.comit.practice._03_practice;

public class Ex10 {

	/*
	 * 10.	Write a program in Java that prints the squares of the first 30 natural numbers in the console.
	 */
	public static void main(String[] args) {
		for(int i=1; i<=30; i++) {
			System.out.printf("The squre of %2d is %3d%n", i, i*i);
		}

	}

}
